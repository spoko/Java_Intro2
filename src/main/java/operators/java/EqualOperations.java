package operators.java;

public class EqualOperations {
    public static void main(String[] args) {
        int firstNumber = 5;
        int secondNumber = 2;

        //equals
        System.out.println(firstNumber == secondNumber);

        //not equals
        System.out.println(firstNumber != secondNumber);


        System.out.println(firstNumber >= secondNumber);
    }
}
